    public void resetGUI() {
        try {
            ledigDatoListe.setModel(new DefaultListModel());
            elevListe.setModel(new DefaultListModel());
        } catch (Exception e) {
        }
        dato.setText("Vælg dato");
        kategori.setModel(new DefaultComboBoxModel());
        korelaerer.setModel(new DefaultComboBoxModel());
        tilfojElev.setText("");
        dagsHandler.resetElever();
        book.setEnabled(false);
        fjernElev.setEnabled(false);
        dagsHandler.resetElever();
        tilfoj.setEnabled(true);
        tilfojElev.setEditable(true);
    }

    public void populerGUI() {
        populerKategorier();
        opdaterElevListe();
        opdaterDatoListe();
        opdaterKorelaererListe();
    }

    public void populerKategorier() {
        DefaultComboBoxModel dcbm = new DefaultComboBoxModel();
        dcbm.addElement("A");
        dcbm.addElement("B");
        dcbm.addElement("C");
        dcbm.addElement("D");
        kategori.setModel(dcbm);
    }

    public void opdaterElevListe() {
        DefaultListModel dlm = new DefaultListModel();

        for (String s : dagsHandler.hentElever()) {
            dlm.addElement(s);
        }

        elevListe.setModel(dlm);
    }

    public void opdaterDatoListe() {
        DefaultListModel dlm = new DefaultListModel();

        for (String s : dagsHandler.startKursusBooking()) {
            dlm.addElement(s);
        }

        ledigDatoListe.setModel(dlm);
    }

    public void opdaterKorelaererListe() {
        DefaultComboBoxModel dcbm = new DefaultComboBoxModel();

        for (Korelaerer k : dagsHandler.hentKorelaerer()) {
            dcbm.addElement(k.getNavn());
        }

        korelaerer.setModel(dcbm);
    }

