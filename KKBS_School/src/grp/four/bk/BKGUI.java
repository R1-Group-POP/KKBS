    private void tilfojActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tilfojActionPerformed
        String input = tilfojElev.getText();
        if (!input.isEmpty()) {
            dagsHandler.tilføjElev(input);
            tilfojElev.setText("");
            if (dagsHandler.elevListeEr6()) {
                tilfojElev.setEditable(false);
                tilfoj.setEnabled(false);
            }
            if (!dagsHandler.elevListeEr0()) {
                fjernElev.setEnabled(true);
            }
            opdaterElevListe();
        } else {
            JOptionPane.showMessageDialog(this,
                    "Du skal skrive et navn i feltet",
                    "FEJL",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tilfojActionPerformed

    private void fjernElevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fjernElevActionPerformed
        dagsHandler.fjernElev(elevListe.getSelectedValue().toString());
        if (!dagsHandler.elevListeEr6()) {
            tilfojElev.setEditable(true);
            tilfoj.setEnabled(true);
        }
        if (dagsHandler.elevListeEr0()) {
            fjernElev.setEnabled(false);
        }
        opdaterElevListe();
    }//GEN-LAST:event_fjernElevActionPerformed

    private void bookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bookActionPerformed
        if (dagsHandler.korerlaererKatalogEr0()) {
            JOptionPane.showMessageDialog(this,
                    "Du har ingen kørelærer registreret i systemet!",
                    "FEJL",
                    JOptionPane.ERROR_MESSAGE);
        } else if (!dagsHandler.elevListeEr6()) {
            JOptionPane.showMessageDialog(this,
                    "Du SKAL have 6 elever for at kunne booke et kursus!",
                    "FEJL",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            String[] elever = new String[dagsHandler.hentElever().size()];
            dagsHandler.hentElever().toArray(elever);
            dagsHandler.angivKursusOplysninger(dato.getText(), kategori.getSelectedItem().toString(), dagsHandler.hentKorelaerer(korelaerer.getSelectedItem().toString()), elever);
            resetGUI();
            populerGUI();
        }
    }//GEN-LAST:event_bookActionPerformed
